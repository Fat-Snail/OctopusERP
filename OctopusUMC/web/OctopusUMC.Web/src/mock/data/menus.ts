export function flattenMenus(menus: MenuResponse[]): MenuResponse[] {
  const result: MenuResponse[] = []
  function walk(list: MenuResponse[]) {
    for (const m of list) {
      result.push(m)
      if (m.children?.length) walk(m.children)
    }
  }
  walk(menus)
  return result
}
